<?php

$a = 60;
$b = 13;

//Bitwise AND
$c = $a & $b;
echo "$a & $b = $c";
echo "<hr>";

//Bitwise OR
$c = $a | $b;
echo "$a | $b = $c";
echo "<hr>";

//Bitwise XOR
$c = $a ^ $b;
echo "$a ^ $b = $c";
echo "<hr>";

//Shift Left
$c = $a << $b;
echo "$a << $b = $c";
echo "<hr>";

//Bitwise Right
$c = $a >> $b;
echo "$a >> $b = $c";
echo "<hr>";

?>